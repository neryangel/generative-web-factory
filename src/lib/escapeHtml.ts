/**
 * Escape HTML special characters to prevent XSS in HTML templates
 * Mirrors the Edge Function validation.ts escapeHtml for frontend use
 */
export function escapeHtml(value: string): string {
  return value
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
